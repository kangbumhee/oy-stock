var Regions = {
  _selectorBound: false,

  renderSelector: function () {
    var sel = document.getElementById('region-select');
    if (!sel || typeof App === 'undefined' || !App.regions) return;
    sel.innerHTML =
      '<option value="">빠른 지역</option>' +
      App.regions
        .map(function (r, i) {
          return '<option value="' + i + '">' + String(r.n).replace(/</g, '&lt;').replace(/"/g, '&quot;') + '</option>';
        })
        .join('');
    if (this._selectorBound) return;
    this._selectorBound = true;
    sel.addEventListener('change', function () {
      var idx = parseInt(sel.value, 10);
      if (isNaN(idx) || !App.regions[idx]) return;
      var reg = App.regions[idx];
      App.regionName = reg.n;
      App.lat = reg.lat;
      App.lng = reg.lng;
      App._save();
      App._updateRegion();
      var ri = document.getElementById('region-input');
      if (ri) ri.value = reg.n;
      sel.value = '';
      var q = document.getElementById('search-input');
      if (q && q.value.trim()) App.doSearch(q.value.trim());
    });
  }
};
